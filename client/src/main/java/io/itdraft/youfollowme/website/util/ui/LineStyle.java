package io.itdraft.youfollowme.website.util.ui;

public class LineStyle {
    private String color;
    private int width;

    public LineStyle(String color, int width) {
        this.color = color;
        this.width = width;
    }

    public String getColor() {
        return color;
    }

    public int getWidth() {
        return width;
    }
}
